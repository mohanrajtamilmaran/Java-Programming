package week2.day2;

public class PrintingNumbers {

	public static void main(String[] args) {
		// Syntax for For loop
		// for(initialization;Condition;increment or decrement)
		// {
		// Statememts;
		// }
		for (int i = 0; i < 5; i++) {

			System.out.println(i);

		}
		System.out.println("Second Iteration");
		for(int i=5;i>0;i--)
		{
			System.out.println(i);
		}
	}
}
