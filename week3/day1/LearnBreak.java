package week3.day1;

public class LearnBreak {
	public static void main(String[] args) {
		int n=5; 
for(int i =0;i<n;i++)
{
	if(i==4)
	{
		continue;
	}
	System.out.println(i);
	 if(i==3)
	{
		break;
	}
	System.out.println(i);		
}
}
}
